#str methods

name = "terry"

print(len(name)) #descreve o tamanho da string
print(name.find("t")) #procura pelo caracter
print(name.capitalize()) #coloca o primeira letra do no 
print(name.upper()) #coloca a palavra em caixa alta
print(name.lower()) #coloca a palavra em caixa baixa
print(name.isdigit()) #verifica se é um digito
print(name.isalpha()) #verifica se contém apenas caracteres alfabéticos
print(name.count("t")) #conta os caracteres
print(name.replace("t", "k")) #substitui o caracter 
print(name*3) #displays o nome 3 vezes